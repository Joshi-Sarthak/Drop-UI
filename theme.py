def hsl_to_rgb(h, s, l):
    return colorsys.hls_to_rgb(h / 360, l, s)


def lab_to_oklab(lab):
    # Approximate conversion from Lab to OKLab
    L, a, b = lab
    return (L / 100, a * 0.5, b * 0.5)  # Simplified conversion


def rgb_to_oklch(r, g, b):
    # Convert RGB (0-1) to Lab
    lab = rgb2lab(np.array([[[r, g, b]]])).reshape(3)

    # Convert Lab to OKLab
    oklab = lab_to_oklab(lab)

    # Convert OKLab to OKLCH
    L, a, b = oklab
    C = np.sqrt(a**2 + b**2)
    H = np.degrees(np.arctan2(b, a)) % 360

    return (L, C, H)


def hsl_to_oklch(h, s, l):
    r, g, b = hsl_to_rgb(h, s, l)
    return rgb_to_oklch(r, g, b)


